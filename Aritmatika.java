public class Aritmatika {
    public static void main(String[] args) {
        // +, -, *, /, %
        int x = 100;
        int y = 20;
        System.out.println(x + y);//120
        System.out.println(x - y);//80
        System.out.println(x * y);//2000
        System.out.println(x / y);//5
        System.out.println(x % y);//0
    }
}
